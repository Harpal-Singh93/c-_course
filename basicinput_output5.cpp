#include<iostream>
using namespace std;
int main()
{
int num1,num2;
cout<<"enter the first number : \n";   //   '<<' is called as insertion operator
cin>>num1;     // '>>' is called as extraction operator
cout<<"\nenter the second number : \n";   //   '<<' is called as insertion operator
cin>>num2;     // '>>' is called as extraction operator
cout<<"\nthe sum is  "<<num1+num2;
    return 0;
}